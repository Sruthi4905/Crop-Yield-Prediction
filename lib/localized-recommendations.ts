export function getLocalizedRecommendations(language: string, category: string, cropType: string, cropHealth?: string) {
  const langRecommendations = localizedRecommendations[language as keyof typeof localizedRecommendations]

  if (!langRecommendations) {
    return null // No recommendations available for this language
  }

  if (category === "generalAdvice" && cropHealth) {
    return (
      langRecommendations.generalAdvice?.[cropType as keyof typeof langRecommendations.generalAdvice]?.[
        cropHealth as keyof any
      ] || null
    )
  }

  if (category === "pestManagement") {
    return langRecommendations.pestManagement?.[cropType as keyof typeof langRecommendations.pestManagement] || null
  }

  return null
}
